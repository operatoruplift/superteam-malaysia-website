// Generate dots
const dots = [];
for (let r = 0; r < ROWS; r++) {
  for (let c = 0; c < COLS; c++) {
    const lon = colToLon(c + 0.5);
    const lat = rowToLat(r + 0.5);
    let isLand = false;
    for (const cont of continents) {
      if (pointInPolygon(lon, lat, cont.poly)) {
        isLand = true;
        break;
      }
    }
    if (isLand) {
      dots.push([c, r]);
    }
  }
}

