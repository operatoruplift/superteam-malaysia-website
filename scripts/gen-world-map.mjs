// Generate high-resolution world map dots using simplified continent polygons
// Grid: 160 cols × 80 rows, dot radius 0.15

const COLS = 160;
const ROWS = 80;
const LON_MIN = -170;
const LON_MAX = 180;
const LAT_MAX = 72;
const LAT_MIN = -60;

function colToLon(c) { return LON_MIN + (c / COLS) * (LON_MAX - LON_MIN); }
function rowToLat(r) { return LAT_MAX - (r / ROWS) * (LAT_MAX - LAT_MIN); }

// Ray-casting point-in-polygon
function pointInPolygon(x, y, poly) {
  let inside = false;
  for (let i = 0, j = poly.length - 1; i < poly.length; j = i++) {
    const xi = poly[i][0], yi = poly[i][1];
    const xj = poly[j][0], yj = poly[j][1];
    if ((yi > y) !== (yj > y) && x < ((xj - xi) * (y - yi)) / (yj - yi) + xi) {
      inside = !inside;
    }
  }
  return inside;
}

